package Modelo;

public class Inventario {
  
    private String Nume_Inve;
    private String Nomb_Inve;
    private String Desc_Inve;
    private long Cant_Inve;

    public String getNume_Inve() {
        return Nume_Inve;
    }

    public void setNume_Inve(String Nume_Inve) {
        this.Nume_Inve = Nume_Inve;
    }

    public String getNomb_Inve() {
        return Nomb_Inve;
    }

    public void setNomb_Inve(String Nomb_Inve) {
        this.Nomb_Inve = Nomb_Inve;
    }

    public String getDesc_Inve() {
        return Desc_Inve;
    }

    public void setDesc_Inve(String Desc_Inve) {
        this.Desc_Inve = Desc_Inve;
    }

    public long getCant_Inve() {
        return Cant_Inve;
    }

    public void setCant_Inve(long Cant_Inve) {
        this.Cant_Inve = Cant_Inve;
    }
    
    
}
